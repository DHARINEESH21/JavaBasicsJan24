package basicSamples;

public class OperationSample {
	public static void main(String[] args) {
		//declare variable
		int a=12, b=3;
		
		//addition operator
		System.out.println("a+b= "+(a+b));
		
		//subtraction operator
		System.out.println("a-b= "+(a-b));
		
		//multiplication operator
		System.out.println("a*b= "+(a*b));
		
		//division operator
		System.out.println("a/b= "+(a/b));
		
		//modulo operator
		System.out.println("a%b= "+(a%b));
		
		//CREATE VARIABLES
		int ac=4;
		int var;
		
		//ASSIGN VALUE USING +
		
		var = ac;
		System.out.println("VAR USING =: "+ var);
		
		//ASSIGN VALUE USING =+
		
		var += ac;
		System.out.println("VAR USING +=: "+ var);
		
		//ASSIGN VALUE USING =*
		
		var *= ac;
		System.out.println("VAR USING *=: "+ var);
		
	}

}
